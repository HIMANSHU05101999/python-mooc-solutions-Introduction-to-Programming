num1=int(input("Number 1: "))
num2=int(input("Number 2: "))
ope=input("Operation: ")

if (ope=="add"):
    print(f"{num1} + {num2} = {num1+num2}")
if (ope=="subtract"):
    print(f"{num1} - {num2} = {num1-num2}")
if (ope=="multiply"):
    print(f"{num1} * {num2} = {num1*num2}")